     */
    public function store(Request $request)
    {
        $obj = new Category();
        $obj->name = Input::get('name');
        $obj->thumbnail = Input::get('thumbnails');
        $obj->description = Input::get('description');
        $obj->parentId = Input::get('game');
        $obj->save();
        return redirect('admin/category');
    }

